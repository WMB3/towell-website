export function retrieveRelevantContext(message, language = 'en') {
  const messageLower = String(message || '').toLowerCase();
  const relevantEvents = [];
  const relevantSectors = [];

  for (const event of heritageKnowledge.timeline) {
    const score = event.keywords.filter((keyword) => messageLower.includes(keyword.toLowerCase())).length;
    if (score > 0 || messageLower.includes(String(event.year))) {
      relevantEvents.push({ ...event, score });
    }
  }

  for (const sector of heritageKnowledge.sectors) {
    const score = sector.keywords.filter((keyword) => messageLower.includes(keyword.toLowerCase())).length;
    if (score > 0) relevantSectors.push({ ...sector, score });
  }

  relevantEvents.sort((a, b) => b.score - a.score);
  relevantSectors.sort((a, b) => b.score - a.score);

  let contextText = '';

  if (relevantEvents.length > 0) {
    contextText += 'TIMELINE EVENTS:\n';
    for (const event of relevantEvents.slice(0, 3)) {
      contextText += `${event.year}: ${language === 'ar' ? event.titleAr : event.title}\n`;
      contextText += `${language === 'ar' ? event.descriptionAr : event.description}\n\n`;
    }
  }

  if (relevantSectors.length > 0) {
    contextText += 'BUSINESS SECTORS:\n';
    for (const sector of relevantSectors.slice(0, 2)) {
      contextText += `${language === 'ar' ? sector.nameAr : sector.name}: `;
      contextText += `${language === 'ar' ? sector.descriptionAr : sector.description}\n\n`;
    }
  }

  if (!contextText) {
    contextText =
      language === 'ar'
        ? 'مجموعة تاول شركة عائلية عُمانية تأسست عام 1866 وتعمل عبر قطاعات متعددة في عُمان والإمارات والهند.'
        : 'Towell Group is an Omani family business established in 1866, operating across multiple sectors in Oman, UAE, and India.';
  }

  return {
    context: contextText,
    events: relevantEvents.slice(0, 2).map((e) => ({
      year: e.year,
      title: language === 'ar' ? e.titleAr : e.title
    })),
    sources:
      relevantEvents.length > 0
        ? ['Towell Timeline 1866-2026', 'wjtowell.com/history']
        : ['wjtowell.com']
  };
}

